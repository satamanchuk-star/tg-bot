# Обратная совместимость: плоский текст для мест, где используется ADMIN_HELP
ADMIN_HELP = "\n\n".join(text for _, text in ADMIN_CATEGORIES.values())


def admin_menu_keyboard() -> InlineKeyboardMarkup:
    """Главное меню категорий для /admin."""
    buttons = [
        InlineKeyboardButton(text=label, callback_data=f"adm:{key}")
        for key, (label, _) in ADMIN_CATEGORIES.items()
    ]
    # По 2 кнопки в строку
    rows = [buttons[i : i + 2] for i in range(0, len(buttons), 2)]
    return InlineKeyboardMarkup(inline_keyboard=rows)


def admin_back_keyboard() -> InlineKeyboardMarkup:
    """Кнопка «Назад» для возврата в главное меню."""
    return InlineKeyboardMarkup(
        inline_keyboard=[
            [InlineKeyboardButton(text="« Назад", callback_data="adm:back")],
        ],
    )
